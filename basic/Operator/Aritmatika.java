public class Aritmatika {
    public static void main(String[] args) {
        int a = 12;
        int b = 8;
        int c = 5;
        
        System.out.println("a + b - c = "+(a+b-c));
        System.out.println("a x b : c = "+(a*b/c));
        System.out.println("a + b x c = "+(a+b*c));
        System.out.println("a + b : c = "+(a+b/c));
        System.out.println("(a + b) x c = "+((a+b)*c));
        System.out.println("(a - b) x c = "+((a-b)*c));
        
    }
    
}
