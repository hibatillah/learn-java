public class ClassSatu1 {
    public int tambah(int num1, int num2){
        return num1 + num2;
    }
    public int kurang(int num1, int num2){
        return num1 - num2;
    }
    public int kali(int num1, int num2){
        return num1 * num2;
    }
    public int bagi(int num1, int num2){
        return num1 / num2;
    }
    public void cetak(String kata){
        System.out.println("Kata yang anda masukkan: "+kata);
    }
    
}
